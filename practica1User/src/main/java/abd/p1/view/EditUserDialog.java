    private void avatarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_avatarBtnActionPerformed
        JFileChooser files = new JFileChooser();
        int result = files.showOpenDialog(this);

        if (result == JFileChooser.APPROVE_OPTION) {

            try {
                FileInputStream input = new FileInputStream(files.getSelectedFile());
                byte[] data = new byte[input.available()];
                input.read(data);
                input.close();
                this.controller.changeAvatar(data);
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "Error al cargar el avatar", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_avatarBtnActionPerformed

