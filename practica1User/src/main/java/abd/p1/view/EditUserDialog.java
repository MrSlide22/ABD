    @Override
    public void onFechaDeNacimientoChange(Date fecha) {
        Calendar cal = new GregorianCalendar();
        cal.setTime(fecha);

        this.edadLabel.setText(Integer.toString(calcularEdad(cal)) + " a침os");
    }

    private int calcularEdad(Calendar fechaNac) {

        Calendar today = Calendar.getInstance();
        int diffYear = today.get(Calendar.YEAR) - fechaNac.get(Calendar.YEAR);
        int diffMonth = today.get(Calendar.MONTH) - fechaNac.get(Calendar.MONTH);
        int diffDay = today.get(Calendar.DAY_OF_MONTH) - fechaNac.get(Calendar.DAY_OF_MONTH);
        // Si est치 en ese a침o pero todav칤a no los ha cumplido
        if (diffMonth < 0 || (diffMonth == 0 && diffDay < 0)) {
            diffYear = diffYear - 1; // no aparec칤an los dos guiones del
        }

        return diffYear;

    }

