    private void passBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_passBtnActionPerformed
        JLabel l1 = new JLabel("Contrase침a");
        JPasswordField pass1 = new JPasswordField();
        JLabel l2 = new JLabel("Repite la contrase침a");
        JPasswordField pass2 = new JPasswordField();

        Object[] params = {l1, pass1, l2, pass2};
        if (JOptionPane.showConfirmDialog(this, params, "Cambiar contrase침a", JOptionPane.PLAIN_MESSAGE) == JOptionPane.OK_OPTION) {

            if (String.valueOf(pass1.getPassword()).equals(String.valueOf(pass2.getPassword()))) {
                this.controller.changePassword(String.valueOf(pass1.getPassword()));
            } else {
                JOptionPane.showMessageDialog(this, "Las contrase침as no coinciden", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_passBtnActionPerformed

